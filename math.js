function double(n){
    return n + n;
}

function square(n){
    return n * n;
}

function isEven(n){
    if (n % 2 === 0) {
        return n + " is even";
    }
    else
        return n + " is not even";
}

function isOdd(n){
    if (n % 2 > 0)
        return n + " is odd";
    else
        return n + " is not odd";
}

function power(n, n2){
    return n ** n2;
}

function multiply(n, n2){
    return n * n2;
}